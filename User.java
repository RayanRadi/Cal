public class User {
    public String name;
    public double weight;
    public int age;
    public String gender;
    public double dailyGoal;

    public User(String name, double weight, int age, String gender, double dailyGoal) {
        this.name = name;
        this.weight = weight;
        this.age = age;
        this.gender = gender;
        this.dailyGoal = dailyGoal;
    }
}